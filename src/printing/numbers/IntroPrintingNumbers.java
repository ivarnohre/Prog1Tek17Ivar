package printing.numbers;

public class IntroPrintingNumbers {

	public static void main(String[] args) {
		System.out.println("74+36");
		System.out.println(74 + 36);
		System.out.println(74.0 / 36.0);
		System.out.println(74d / 36);
		System.out.println(74f / 36);
		System.out.println(Math.pow(12, 3));
		System.out.println(56 % 24);
		
		System.out.println("timmar:" + 375 / 60);
		System.out.println("minuter:" + 375 % 60);
		
		System.out.println("Tal:" + (74 + 36));
		}

}
