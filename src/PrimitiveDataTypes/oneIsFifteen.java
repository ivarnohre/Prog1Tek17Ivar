package PrimitiveDataTypes;

public class oneIsFifteen {

	public static void main(String[] args) {
		//Change integers to test the code
		int x = 1;
		int y = 14;
		
		if (x + y == 15) {
			System.out.println("True");
		} else if (x == 15) {
			System.out.println("True");
		} else if (y == 15) {
			System.out.println("True");
		}else if (x - y == 15) {
			System.out.println("True");
		}else if (y - x == 15) {
			System.out.println("True");
		} else {
			System.out.println("False");
		}
		
		

	}

}
